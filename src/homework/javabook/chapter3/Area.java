package homework.javabook.chapter3;

public class Area {
    public static void main(String[] args) {
        double pi, r, a;
        r = 10.8; // շրջանագծի շառավիղը
        pi = 3.1416; // pi-ի մոտավոր արժեքը
        a = pi * r * r; // հաշվարկել շրջանագծի մակերեսը
        System.out.println("Շրջանակի մակերեսն է " + a);
    }
}
